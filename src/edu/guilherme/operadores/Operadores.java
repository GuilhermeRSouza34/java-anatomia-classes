package edu.guilherme.operadores;

public class Operadores {
    public static void main(String[] args) {
        // Exemplo de operador de atribuição
        int a = 5;

        // Exemplo de operador de adição
        int soma = 3 + 4;

        // Exemplo de operador de subtração
        int diferenca = 7 - 2;

        // Exemplo de operador de multiplicação
        int produto = 6 * 5;

        // Exemplo de operador de divisão
        double quociente = 10.0 / 2;

        // Exemplo de operador de módulo
        int resto = 10 % 3;

        // Exemplo de operador de incremento
        int x = 5;
        x++;

        // Exemplo de operador de decremento
        int y = 8;
        y--;

        // Exemplo de operador de igualdade
        boolean igual = (5 == 5);

        // Exemplo de operador de desigualdade
        boolean diferente = (5 != 3);

        // Exemplo de operador de maior que
        boolean maior = (10 > 5);

        // Exemplo de operador de menor que
        boolean menor = (3 < 7);

        // Exemplo de operador de maior ou igual
        boolean maiorOuIgual = (8 >= 8);

        // Exemplo de operador de menor ou igual
        boolean menorOuIgual = (4 <= 6);

        // Exemplo de operador lógico AND
        boolean condicaoAnd = (5 > 3 && 6 < 10);

        // Exemplo de operador lógico OR
        boolean condicaoOr = (3 < 2 || 8 > 6);

        // Exemplo de operador lógico NOT
        boolean condicaoNot = !(4 == 5);

        // Exemplo de operador de atribuição de adição
        int z = 3;
        z += 2;

        // Exemplo de operador de atribuição de subtração
        int w = 7;
        w -= 4;

        // Exemplo de operador de atribuição de multiplicação
        int v = 6;
        v *= 3;

        // Exibindo resultados
        System.out.println("Exemplos de operadores em Java:");
        System.out.println("Soma: " + soma);
        System.out.println("Diferença: " + diferenca);
        System.out.println("Produto: " + produto);
        System.out.println("Quociente: " + quociente);
        System.out.println("Resto: " + resto);
        System.out.println("Incremento de x: " + x);
        System.out.println("Decremento de y: " + y);
        System.out.println("Igualdade: " + igual);
        System.out.println("Desigualdade: " + diferente);
        System.out.println("Maior que: " + maior);
        System.out.println("Menor que: " + menor);
        System.out.println("Maior ou igual: " + maiorOuIgual);
        System.out.println("Menor ou igual: " + menorOuIgual);
        System.out.println("Condição AND: " + condicaoAnd);
        System.out.println("Condição OR: " + condicaoOr);
        System.out.println("Condição NOT: " + condicaoNot);
        System.out.println("Atribuição de adição: " + z);
        System.out.println("Atribuição de subtração: " + w);
        System.out.println("Atribuição de multiplicação: " + v);
    }
}
